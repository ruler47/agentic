    "  const response = await fetch(absoluteUrl);",
    "  if (!response.ok) throw new Error(`artifact download failed: ${response.status}`);",
    "  const mimeType = typeof artifact.mimeType === \"string\" ? artifact.mimeType : response.headers.get(\"content-type\") ?? \"application/octet-stream\";",
