export function renderToolSource(input: ToolCreationV1Result["input"], manifest: Omit<ToolPackageManifest, "package">): string {
  if (input.kind === "browser-screenshot") {
    return renderBrowserScreenshotToolSource(input, manifest);
  }
  if (input.kind === "browser-operate" || input.kind === "external-action-prepare") {
    return renderBrowserOperateToolSource(input, manifest);
  }
  if (input.kind === "web-search") {
    return renderWebSearchToolSource(input, manifest);
  }
  if (input.kind === "web-read") {
    return renderWebReadToolSource(input, manifest);
  }
  if (input.kind === "external-action-commit") return renderExternalActionCommitToolSource(input, manifest);
  if (input.kind === "npm-default-function") {
    const packageName = input.adapterPackageName ?? Object.keys(input.dependencies)[0];
    if (!packageName) throw new Error("npm-default-function tools require at least one dependency");
    const adapterContract = input.adapterContract ?? {
      packageName,
      importStyle: "default" as const,
      inputMode: "text-options" as const,
      evidence: "Default callable adapter contract.",
    };
    return [
      "import type { Tool } from \"../tool.js\";",
      `import * as packageModule from ${JSON.stringify(packageName)};`,
      "",
      `const inputSchema: Tool["inputSchema"] = ${JSON.stringify(manifest.inputSchema, null, 2)};`,
      `const outputSchema: Tool["outputSchema"] = ${JSON.stringify(manifest.outputSchema, null, 2)};`,
      "type AdapterContract = {",
      "  packageName: string;",
      "  importStyle: \"default\" | \"named\" | \"namespace\";",
      "  exportName?: string;",
      "  memberName?: string;",
      "  inputMode: \"text-options\" | \"object\";",
      "  inputSchema?: { type: \"object\"; properties: Record<string, unknown>; required?: string[] };",
      "  inputExample?: Record<string, unknown>;",
      "  evidence: string;",
      "};",
      `const adapterContract: AdapterContract = ${JSON.stringify(adapterContract, null, 2)};`,
      "",
      "type CallablePackage = (...args: unknown[]) => unknown;",
      "",
      "function resolveCallable(moduleValue: unknown): CallablePackage | undefined {",
      "  const record = moduleValue && typeof moduleValue === \"object\" ? moduleValue as Record<string, unknown> : {};",
      "  const defaultRecord = record.default && typeof record.default === \"object\"",
      "    ? record.default as Record<string, unknown>",
      "    : {};",
      "  const candidate = adapterContract.importStyle === \"named\"",
      "    ? record[adapterContract.exportName ?? \"\"] ?? defaultRecord[adapterContract.exportName ?? \"\"]",
      "    : adapterContract.importStyle === \"namespace\"",
      "      ? record[adapterContract.memberName ?? \"\"] ?? defaultRecord[adapterContract.memberName ?? \"\"]",
      "      : record.default ?? moduleValue;",
      "  if (typeof candidate === \"function\") {",
      "    return candidate as CallablePackage;",
      "  }",
      "  return undefined;",
      "}",
      "",
      "export const tool: Tool = {",
      `  name: ${JSON.stringify(input.name)},`,
      `  displayName: ${JSON.stringify(input.displayName)},`,
      `  version: ${JSON.stringify(input.version)},`,
      `  description: ${JSON.stringify(input.description)},`,
      `  capabilities: ${JSON.stringify(input.capabilities)},`,
      `  startupMode: ${JSON.stringify(manifest.startupMode)},`,
      "  inputSchema,",
      "  outputSchema,",
      "  async healthcheck() {",
      "    const callable = resolveCallable(packageModule);",
      `    return callable ? { ok: true, detail: ${JSON.stringify(`${packageName} adapter contract loaded.`)} } : { ok: false, detail: ${JSON.stringify(`${packageName} did not satisfy the adapter contract.`)} };`,
      "  },",
      "  async run(input) {",
      "    const callable = resolveCallable(packageModule);",
      "    if (!callable) {",
      `      return { ok: false, content: ${JSON.stringify(`${packageName} did not satisfy the adapter contract.`)} };`,
      "    }",
      "    const args = buildCallArguments(input);",
      "    if (!args.ok) return args.result;",
      "    const value = await callable(...args.args);",
      "    const content = typeof value === \"string\" ? value : JSON.stringify(value);",
      "    return { ok: true, content, data: { packageName: " + JSON.stringify(packageName) + ", adapterContract } };",
      "  },",
      "};",
      "",
      "function buildCallArguments(input: Record<string, unknown>): { ok: true; args: unknown[] } | { ok: false; result: { ok: false; content: string } } {",
      "  if (adapterContract.inputMode === \"object\") {",
      "    const missing = (adapterContract.inputSchema?.required ?? []).filter((key) => input[key] === undefined);",
      "    if (missing.length > 0) {",
      "      return { ok: false, result: { ok: false, content: `missing required input: ${missing.join(\", \")}` } };",
      "    }",
      "    return { ok: true, args: [input] };",
      "  }",
      "  const text = typeof input.text === \"string\" ? input.text : \"\";",
      "  if (!text.trim()) return { ok: false, result: { ok: false, content: \"text is required\" } };",
      "  const options = input.options && typeof input.options === \"object\" && !Array.isArray(input.options)",
      "    ? input.options as Record<string, unknown>",
      "    : {};",
      "  return { ok: true, args: [text, options] };",
      "}",
      "",
    ].join("\n");
  }
  if (input.kind === "service-adapter") {
    if (
      input.integrationContract?.protocol === "messaging-bot" &&
      input.integrationContract.provider?.toLowerCase() === "telegram"
    ) {
      return renderTelegramMessagingServiceSource(input, manifest);
    }
    return [
      "import type { Tool } from \"../tool.js\";",
      "",
      `const inputSchema: Tool["inputSchema"] = ${JSON.stringify(manifest.inputSchema, null, 2)};`,
      `const outputSchema: Tool["outputSchema"] = ${JSON.stringify(manifest.outputSchema, null, 2)};`,
      `const integration = ${JSON.stringify(input.integrationContract, null, 2)} as any;`,
      "",
      "export const tool: Tool = {",
      `  name: ${JSON.stringify(input.name)},`,
      `  displayName: ${JSON.stringify(input.displayName)},`,
      `  version: ${JSON.stringify(input.version)},`,
      `  description: ${JSON.stringify(input.description)},`,
      `  capabilities: ${JSON.stringify(input.capabilities)},`,
      `  startupMode: ${JSON.stringify(manifest.startupMode)},`,
      "  inputSchema,",
      "  outputSchema,",
      "  async healthcheck() {",
      "    return { ok: true, detail: `Service adapter contract loaded (${integration?.protocol ?? \"custom\"}).` };",
      "  },",
      "  async startService(context) {",
      "    return {",
      "      async stop() {",
      "        // The scaffold has no provider loop yet. LLM/provider authoring must replace this with a real adapter.",
      "      },",
      "      async healthcheck() {",
      "        return { ok: true, detail: \"Service adapter scaffold is running without a provider loop.\" };",
      "      },",
      "    };",
      "  },",
      "  async run(input) {",
      "    const event = normalizeManualEvent(input);",
      "    return {",
      "      ok: true,",
      "      content: event.text ? `Accepted integration event: ${event.text}` : \"Accepted integration event.\",",
      "      data: { event, integration },",
      "    };",
      "  },",
      "};",
      "",
      "function normalizeManualEvent(input: Record<string, unknown>): Record<string, unknown> {",
      "  const event = input.event && typeof input.event === \"object\" && !Array.isArray(input.event)",
      "    ? input.event as Record<string, unknown>",
      "    : {};",
      "  return {",
      "    ...event,",
      "    text: typeof input.text === \"string\" ? input.text : typeof event.text === \"string\" ? event.text : undefined,",
      "    sourceChannelId: typeof input.sourceChannelId === \"string\" ? input.sourceChannelId : event.sourceChannelId,",
      "    sourceUserId: typeof input.sourceUserId === \"string\" ? input.sourceUserId : event.sourceUserId,",
      "  };",
      "}",
      "",
    ].join("\n");
  }
  if (input.kind === "http-json") {
    return renderHttpJsonToolSource(input, manifest);
  }
  return [
    "import type { Tool } from \"../tool.js\";",
    "",
    `const inputSchema: Tool["inputSchema"] = ${JSON.stringify(manifest.inputSchema, null, 2)};`,
    `const outputSchema: Tool["outputSchema"] = ${JSON.stringify(manifest.outputSchema, null, 2)};`,
    "",
    "export const tool: Tool = {",
    `  name: ${JSON.stringify(input.name)},`,
    `  displayName: ${JSON.stringify(input.displayName)},`,
    `  version: ${JSON.stringify(input.version)},`,
    `  description: ${JSON.stringify(input.description)},`,
    `  capabilities: ${JSON.stringify(input.capabilities)},`,
    `  startupMode: ${JSON.stringify(manifest.startupMode)},`,
    "  inputSchema,",
    "  outputSchema,",
    "  async healthcheck() {",
    "    return { ok: true, detail: \"Tool package loaded.\" };",
    "  },",
    "  async run(input) {",
    "    const text = typeof input.text === \"string\" ? input.text : JSON.stringify(input);",
    "    return { ok: true, content: text, data: { echoed: input } };",
    "  },",
    "};",
    "",
  ].join("\n");
}

